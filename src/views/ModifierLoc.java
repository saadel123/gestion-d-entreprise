    private void formInternalFrameOpened(javax.swing.event.InternalFrameEvent evt) {//GEN-FIRST:event_formInternalFrameOpened
        // TODO add your handling code here:
        try {
            // TODO add your handling code here:
            Connection con= UtilConnexion.seConnecter();
            Statement s=con.createStatement();
            
            ResultSet r=s.executeQuery("select * from location");
            while(r.next()){
               
                combo.addItem(new Location(r.getInt("ID_LOC"),
              r.getString("RUE_ADRESSE"),r.getString("VILLE"),r.getInt("CODE_POSTAL")));
            }
            s.close();
            r.close();
            
            
            Statement st2=con.createStatement();
            ResultSet rs2=st2.executeQuery("select * from location");
            while(rs2.next()){
            lg.add(new Location(rs2.getInt("ID_LOC"),
              rs2.getString("RUE_ADRESSE"),rs2.getString("VILLE"),rs2.getInt("CODE_POSTAL")));
            }
            Afficher();
            st2.close();
            rs2.close();
            con.close();
            
        } catch (Exception ex) {
            Logger.getLogger(ModifierLoc.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_formInternalFrameOpened

