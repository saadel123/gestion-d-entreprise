    private void formInternalFrameOpened(javax.swing.event.InternalFrameEvent evt) {//GEN-FIRST:event_formInternalFrameOpened

        try {
            // TODO add your handling code here:
            Connection con = UtilConnexion.seConnecter();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from location");
            while (rs.next()) {
                cbg.addItem(new Location(rs.getInt("id_loc"), rs.getString("rue_adresse"), rs.getString("ville"), rs.getInt("code_postal")));

            }
            rs.close();
            Statement s = con.createStatement();
            ResultSet r = s.executeQuery("select * from client join location using(id_loc)");
            while (r.next()) {

                combo.addItem(new Client(r.getInt("idclt"), r.getString("nom"), r.getString("prenom"), r.getDate("date_rend"), r.getString("email"), r.getString("tel"), r.getInt("prix_ser"), r.getInt("VERSEMENT"),
                        (new Location(r.getInt("id_loc"), r.getString("rue_Adresse"), r.getString("ville"), r.getInt("code_postal")))));
            }
            s.close();
            r.close();
            Statement st2 = con.createStatement();
            ResultSet rs2 = st.executeQuery("select * from client  join location using(id_loc)");
            while (rs2.next()) {
                le.add(new Client(rs2.getInt("idclt"), rs2.getString("nom"), rs2.getString("prenom"), rs2.getDate("date_rend"), rs2.getString("email"), rs2.getString("tel"), rs2.getInt("prix_ser"), rs2.getInt("VERSEMENT"),
                        (new Location(rs2.getInt("id_loc"), rs2.getString("rue_Adresse"), rs2.getString("ville"), rs2.getInt("code_postal")))));
            }
            Afficher();
            st2.close();
            rs2.close();
            con.close();
        } catch (Exception ex) {
            Logger.getLogger(ModifierClt.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_formInternalFrameOpened

