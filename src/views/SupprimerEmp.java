    private void formInternalFrameOpened(javax.swing.event.InternalFrameEvent evt) {//GEN-FIRST:event_formInternalFrameOpened
        // TODO add your handling code here:
        try {
            // TODO add your handling code here:
            Connection con = UtilConnexion.seConnecter();
            Statement s = con.createStatement();

            ResultSet r = s.executeQuery("select * from employee");
            while (r.next()) {

                combo.addItem(new Employee(r.getInt("idemp"), r.getString("nom"), r.getString("prenom"),
                        r.getString("adresse"), r.getString("tel"), r.getString("email"), r.getString("departement"),
                        r.getInt("salary"), r.getInt("rib")));
            }
            s.close();
            r.close();

            Statement st2 = con.createStatement();
            ResultSet rs2 = st2.executeQuery("select * from employee");
            while (rs2.next()) {
                le.add(new Employee(rs2.getInt("idemp"), rs2.getString("nom"), rs2.getString("prenom"),
                        rs2.getString("adresse"), rs2.getString("tel"), rs2.getString("email"), rs2.getString("departement"),
                        rs2.getInt("salary"), rs2.getInt("rib")));
            }
            Afficher();
            st2.close();
            rs2.close();
            con.close();

        } catch (Exception ex) {
            Logger.getLogger(SupprimerEmp.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_formInternalFrameOpened
    private void Afficher() {
        Employee e = le.get(pos);
        txtid.setText(e.getIdemp() + "");
        txtnom.setText(e.getNom());
        txtprenom.setText(e.getPrenom());
    }

