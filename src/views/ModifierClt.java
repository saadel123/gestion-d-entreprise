    private void btnmodifieretudActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnmodifieretudActionPerformed
        // TODO add your handling code here:

        try {
            Client e = le.get(pos);
            e.setNom(txtnom.getText());
            e.setPrenom(txtprenom.getText());
            java.util.Date d = new SimpleDateFormat("yyyy-MM-dd").parse(date_rend.getText());
            java.sql.Date dt = java.sql.Date.valueOf(new SimpleDateFormat("yyyy-MM-dd").format(d));
            e.setLocation((Location) cbg.getSelectedItem());
            e.setEmail(txtemail.getText());
            e.setTel(txttel.getText());
            e.setPrix_ser(Integer.parseInt(txtprix.getText()));
            e.setVersement(Integer.parseInt(txtvers.getText()));
            e.setIdclt(Integer.parseInt(txtid.getText()));

            if (JOptionPane.showConfirmDialog(null, "Voulez vraiment modifier cet Client?") == JOptionPane.YES_OPTION) {
                con = UtilConnexion.seConnecter();
                PreparedStatement ps = con.prepareStatement("update client set nom=?,prenom=?,date_rend=?,email=?,"
                    + "tel=?,prix_ser=?,versement=?,id_loc=? where idclt=?");
                Location grp = (Location) cbg.getSelectedItem();
                ps.setString(1, e.getNom());
                ps.setString(2, e.getPrenom());
                ps.setDate(3, dt);
                ps.setString(4, e.getEmail());
                ps.setString(5, e.getTel());
                ps.setInt(6, e.getPrix_ser());
                ps.setInt(7, e.getVersement());
                ps.setInt(8, grp.getId_loc());
                ps.setInt(9, e.getIdclt());
                ps.executeUpdate();
                con.close();
                JOptionPane.showMessageDialog(null, "CLient modifié avec succée!!");

            }
        } catch (Exception ex) {
            Logger.getLogger(ModifierClt.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnmodifieretudActionPerformed


