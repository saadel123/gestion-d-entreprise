    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String req = "select  ID_LOC,RUE_ADRESSE,VILLE,CODE_POSTAL from location where ";

        if (jRadioButton1.isSelected()) {
            req += " lower(RUE_ADRESSE)like'%" + txtnom.getText().toLowerCase() + "%' ";
        }
        if (jRadioButton2.isSelected()) {
            req += "lower(VILLE)like'%" + txtlangue.getText().toLowerCase() + "%'";
        }
        try {
            con = UtilConnexion.seConnecter();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(req);
            df.setRowCount(0);
            while (rs.next()) {
                df.addRow(new Object[]{
                    rs.getInt(1), rs.getString(2), rs.getString(3), rs.getInt(4)
                });
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Erreur " + ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

