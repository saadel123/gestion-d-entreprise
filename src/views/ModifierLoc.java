    private void btnmodifieretudActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnmodifieretudActionPerformed
        // TODO add your handling code here:
        try {
            Location g = lg.get(pos);
            g.setRue_Adresse(txtadresse.getText());
            g.setVille(txtlville.getText());
            g.setCode_postal(Integer.parseInt(txtcodeposta.getText()));
            if (JOptionPane.showConfirmDialog(null, "Voulez vraiment modifier cette location?") == JOptionPane.YES_OPTION) {
                con = UtilConnexion.seConnecter();
                PreparedStatement ps = con.prepareStatement("update location set RUE_ADRESSE=?,VILLE=?,CODE_POSTAL=? where id_loc=?");
                ps.setString(1, g.getRue_Adresse());
                ps.setString(2, g.getVille());
                ps.setInt(3, g.getCode_postal());
                ps.setInt(4, g.getId_loc());

                ps.executeUpdate();
                con.close();
                JOptionPane.showMessageDialog(null, "Location modifié avec succée!!");

            }
        } catch (Exception ex) {
            Logger.getLogger(ModifierLoc.class.getName()).log(Level.SEVERE, null, ex.getMessage());
        }

    }//GEN-LAST:event_btnmodifieretudActionPerformed

