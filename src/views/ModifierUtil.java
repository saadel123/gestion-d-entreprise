    private void btnmodifieretudActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnmodifieretudActionPerformed
        // TODO add your handling code here:
        try {
            Compte u = lu.get(pos);
            u.setNom(txtnom.getText());
            u.setPrenom(txtprenom.getText());
            u.setUser(txtlogin.getText());
            u.setPassword(txtmdp.getText());
            u.setQUESTION((String) cbQust.getSelectedItem());
            u.setANSWER(txtrepons.getText());
            u.setRole((String) cbrole.getSelectedItem());

            if (JOptionPane.showConfirmDialog(null, "Voulez vraiment modifier cet utilisateur?") == JOptionPane.YES_OPTION) {
                con = UtilConnexion.seConnecter();
                PreparedStatement ps = con.prepareStatement("update compte set nom=?,prenom=?,username=?,password=?,role=?,QUESTION=?,ANSWER=? where iduser=?");
                String role = (String) cbrole.getSelectedItem();
                String quest = (String) cbQust.getSelectedItem();
                ps.setString(1, u.getNom());
                ps.setString(2, u.getPrenom());
                ps.setString(3, u.getUser());
                ps.setString(4, u.getPassword());
                ps.setString(5, role);
                ps.setString(6, quest);
                ps.setString(7, u.getANSWER());
                ps.setInt(8, u.getIduser());
                ps.executeUpdate();
                con.close();
                JOptionPane.showMessageDialog(null, "L'utilisateur modifi√© avec succ√©e!!");

            }
        } catch (Exception ex) {
            Logger.getLogger(ModifierUtil.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnmodifieretudActionPerformed

